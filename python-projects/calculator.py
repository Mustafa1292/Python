def add (n1, n2):
    return n1 + n2

def subtract (n1, n2):
    return n1 - n2

def multiply (n1, n2):
    return n1 * n2

def divide (n1, n2):
    return n1 / n2

operation = { 
    '+': add, 
    '-': subtract, 
    '*': multiply, 
    '/': divide
}

num1 = int(input('enter number 1 '))

for key in operation:
    print(key)

oper = input('Pick an operation ')
num2 = int(input('enter number 2 '))
answer = operation[oper]
x = answer(num1, num2)

print(f'{num1} {oper} {num2} = {x}')